# Data types

a = 10
print(a) # 10
print(type(a)) # <class 'int'>

a = 10.5
print(a) # 10.5
print(type(a)) # <class 'float'>

a = 3 + 4j
print(a) # (3+4j)
print(type(a)) # <class 'complex'>

a = 'hello'
print(a) # hello
print(type(a)) # <class 'str'>

a = True
print(a) # True
print(type(a)) # <class 'bool'>

a = [1, 2, 3]
print(a) # [1, 2, 3]
print(type(a)) # <class 'list'>

a = (1, 2, 3)
print(a) # (1, 2, 3)
print(type(a)) # <class 'tuple'>

a = {1, 2, 3}
print(a) # {1, 2, 3}
print(type(a)) # <class 'set'>  

a = {'name': 'John', 'age': 25}
print(a) # {'name': 'John', 'age': 25}
print(type(a)) # <class 'dict'>

a = None
print(a) # None
print(type(a)) # <class 'NoneType'>

a = b'hello'
print(a) # b'hello'
print(type(a)) # <class 'bytes'>

a = bytearray(b'hello')
print(a) # bytearray(b'hello')
print(type(a)) # <class 'bytearray'>

a = memoryview(b'hello')
print(a) # <memory at 0x0000023E1C3B6B80>
